     private void populateDropdowns() {
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        
        List<Animal> animais = AnimalDAO.getInstance().retrieveAll();
        for (Animal animal : animais) {
            jComboBox1.addItem(animal.getNome());
            
            if (animal.getId() == Controller.getConsultaSelecionada().getAnimalId()) {
                animalSelecionado = animal;
            }
        }

        List<Veterinario> veterinarios = VeterinarioDAO.getInstance().retrieveAll();
        for (Veterinario veterinario : veterinarios) {
            jComboBox2.addItem(veterinario.getNome());
            
            if (veterinario.getId() == Controller.getConsultaSelecionada().getVeterinarioId()) {
                veterinarioSelecionado = veterinario;
            }
        }
        
        if (animalSelecionado != null) {
            jComboBox1.setSelectedItem(animalSelecionado.getNome());
        }
        
        if (veterinarioSelecionado != null) {
            jComboBox2.setSelectedItem(veterinarioSelecionado.getNome());
        }
     }

