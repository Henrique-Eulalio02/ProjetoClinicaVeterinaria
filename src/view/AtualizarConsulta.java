    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String motivo = jTextField1.getText();
        String diagnostico = jTextField2.getText();
        String nomeAnimalelecionado = (String) jComboBox1.getSelectedItem();
        String nomeVeterinarioSelecionado = (String) jComboBox2.getSelectedItem();
        
        Animal animal = (Animal) AnimalDAO.getInstance().retrieveBySimilarName(nomeAnimalelecionado).get(0);
        Veterinario veterinario = (Veterinario) VeterinarioDAO.getInstance().retrieveBySimilarName(nomeVeterinarioSelecionado).get(0);
        
        if (animal != null && veterinario != null) {
            int tutorId = animal.getId();
            int veterinarioId = veterinario.getId();
            
            Controller.atualizarConsulta(motivo, diagnostico, tutorId, veterinarioId);
            animalSelecionado = animal;
            veterinarioSelecionado = veterinario;
        }
        
        Window parentWindow = SwingUtilities.getWindowAncestor(this);
            if (parentWindow instanceof JDialog) {
                ((JDialog) parentWindow).dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

