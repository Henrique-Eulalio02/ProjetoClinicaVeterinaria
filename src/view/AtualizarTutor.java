    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String novoNome = jTextField1.getText();
        String novoEndereco = jTextField2.getText();
        String novoCpf = jTextField3.getText();
        String novoCelular = jTextField4.getText();
        
        if (novoNome.equalsIgnoreCase(Controller.getTutorSelecionado().getNome()) && novoEndereco.equalsIgnoreCase(Controller.getTutorSelecionado().getEndereco()) && novoCpf.equalsIgnoreCase(Controller.getTutorSelecionado().getCpf()) && novoCelular.equalsIgnoreCase(Controller.getTutorSelecionado().getCelular())) {
            JOptionPane.showMessageDialog(this, "Nenhum dado foi atualizado.");
        } else {
            Controller.atualizarTutor(novoNome, novoEndereco, novoCpf, novoCelular);
            
            Window parentWindow = SwingUtilities.getWindowAncestor(this);
            if (parentWindow instanceof JDialog) {
                ((JDialog) parentWindow).dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

